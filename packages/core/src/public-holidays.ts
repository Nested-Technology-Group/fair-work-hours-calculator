/**
 * Check if a given date is a public holiday in the specified state.
 * @param date - Date string in YYYY-MM-DD format
 * @param state - Australian state/territory code
 * @returns true if the date is a public holiday
 */
export function isPublicHoliday(date: string, state: AustralianState): boolean {
  const year = date.substring(0, 4);
  const stateHolidays = holidays[year]?.[state];
  if (!stateHolidays) return false;
  return stateHolidays.includes(date);
}

/**
 * Get all public holidays for a state in a given year.
 * @param state - Australian state/territory code
 * @param year - Calendar year (2024-2026 supported)
 * @returns Array of date strings in YYYY-MM-DD format
 */
export function getPublicHolidays(state: AustralianState, year: number): string[] {
  return holidays[String(year)]?.[state] ?? [];
}
